CREATE DATABASE ESCOLA;

USE ESCOLA;
CREATE TABLE Departamento
(CodDepto INT,
 NomeDepto VARCHAR(30),
 CONSTRAINT PK_Departamento PRIMARY KEY (CodDepto));

CREATE TABLE Aluno
(Matricula INT,
 Nome VARCHAR(10),
 CodDepto INT,
 CONSTRAINT PK_Aluno PRIMARY KEY (Matricula),
 CONSTRAINT FK_AlunoDepartamento FOREIGN KEY (CodDepto) REFERENCES 
Departamento(CodDepto));

CREATE TABLE Disciplina
(CodDisc VARCHAR(7),
 NomeDisc VARCHAR(40),
 Creditos INT,
 CodDepto INT,
 CONSTRAINT PK_Disciplina PRIMARY KEY (CodDisc),
 CONSTRAINT FK_DisciplinaDepartamento FOREIGN KEY (CodDepto) REFERENCES 
Departamento(CodDepto));

CREATE TABLE Turma
(CodTurma INT,
 CodDisc VARCHAR(7),
 Semestre VARCHAR(10),
 Ano INT,
 Professor VARCHAR(10),
 CONSTRAINT PK_Turma PRIMARY KEY (CodTurma),
 CONSTRAINT FK_DisciplinaTurma FOREIGN KEY (CodDisc) REFERENCES 
Disciplina(CodDisc));

CREATE TABLE Historico
(Matricula INT,
 CodTurma INT,
 Conceito VARCHAR(1),
 CONSTRAINT PK_Historico PRIMARY KEY (Matricula,CodTurma),
 CONSTRAINT FK_HistoricoAluno FOREIGN KEY (Matricula) REFERENCES Aluno(Matricula),
 CONSTRAINT FK_HistoricoTurma FOREIGN KEY (CodTurma) REFERENCES Turma(CodTurma));
 
CREATE TABLE Pre_Requisito
(CodDisc VARCHAR(7),
 CodDiscPre VARCHAR(7),
 CONSTRAINT PK_Pre_Requisito PRIMARY KEY (CodDisc,CodDiscPre),
 CONSTRAINT FK_Pre_ReqDisciplina FOREIGN KEY (CodDisc) REFERENCES 
Disciplina(CodDisc),
 CONSTRAINT FK_Pre_ReqDisciplinaPre FOREIGN KEY (CodDiscPre) REFERENCES 
Disciplina(CodDisc));

