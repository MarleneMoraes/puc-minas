T.CODDISC AS 'CODIGO DA DISCILINA', 
D.NOMEDISC AS 'NOME DISCIPLINA', 
D.CREDITOS, T.SEMESTRE, T.ANO, H.CONCEITO FROM ALUNO A 
JOIN HISTORICO H ON H.MATRICULA = A.MATRICULA 
JOIN TURMA T ON T.CODTURMA = H.CODTURMA 
