/* QUESTÃO C */
SELECT D.NOMEDISC AS 'NOME DA DISCIPLINA', 
T.CODTURMA AS 'CODIGO DA TURMA', 
T.SEMESTRE, T.ANO, COUNT(A.NOME) AS 'NUMERO DE ALUNOS' FROM TURMA T 
JOIN DISCIPLINA D ON D.CodDisc = T.CodDisc
JOIN HISTORICO H ON H.CODTURMA = T.CODTURMA 
JOIN ALUNO A ON A.MATRICULA = H.MATRICULA 
WHERE PROFESSOR = 'CARLOS';

/* QUESTÃO D */
